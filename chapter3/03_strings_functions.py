story = "ritik Jain is a very charming boy and he is fabulous in his job."

#length
print(len(story))

#endsWith
print(story.endswith("job."))
print(story.endswith("Ritik"))
# startsWith
print(story.startswith("ri"))

#count a char or word
print(story.count("i"))
print(story.count("is"))

#capitalize the first word
print(story.capitalize())

#find a word
print(story.find("i")) #gives index (only for first occurence)
print(story.find("is"))

#replace
print(story.replace("ritik","Ritik"))
print(story.replace("is","in")) # replaces all occurences






